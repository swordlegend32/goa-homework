#5)ჩამოწერეთ რომელი data type'ბი იცით და ახსენით რა რას ემსახურება

# string სტრინგი არის ნებისმიერი თექსტი ის "" რჭყალებით აღინიშჰნება
string = "i am a string"
print(type(string))
# integer სტრინგი არის ნებისმიერი მთელი რიცხვი 
integer = 1
print(type(integer))
# float სტრინგი არის ნებისმიერი რიცხვი 
float = 1.5
print(type(float))
# boolean ან bool არის 1 ან 0 კომპიუტერში ის აღნიშჰნავს რამე სიმართლე არის თუ არა
boolean = True
print(type(boolean))